import UIKit

extension UIFont {

    class func HelveticaNeueRegular(of size: CGFloat) -> UIFont {
        return UIFont.init(name: "HelveticaNeue", size: size)!
    }

    class func HelveticaNeueBold(of size: CGFloat) -> UIFont {
        return UIFont.init(name: "HelveticaNeue-Bold", size: size)!
    }

}
